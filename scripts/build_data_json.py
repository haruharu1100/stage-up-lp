def extract_region(addr):
    if not addr: return "大阪"
    addr = re.sub(r"^〒\d{3}-\d{4}\s*", "", addr)
    m = re.search(r"大阪市([^\s\d０-９]+?区)", addr)
    if m: return "大阪市" + m.group(1)
    m = re.search(r"堺市([^\s\d０-９]+?区)", addr)
    if m: return "堺市" + m.group(1)
    m = re.search(r"大阪府([^\s\d０-９]+?市)", addr)
    if m: return m.group(1)
    m = re.search(r"奈良市", addr)
    if m: return "奈良市"
    m = re.search(r"奈良県([^\s\d０-９]+?市)", addr)
    if m: return m.group(1)
    m = re.search(r"郡([^\s\d０-９]+?町)", addr)
    if m: return m.group(1)
    return "大阪府"

