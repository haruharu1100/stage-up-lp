def load_tsv(path):
    rows = {}
    if not os.path.exists(path): return rows
    with open(path, "rb") as f:
        raw = f.read().decode("utf-8", errors="replace")
    for line in raw.splitlines():
        if not line.startswith("2026-05-30"): continue
        c = line.split("\t")
        if len(c) == 16:
            try: rows[int(c[1])] = c
            except: pass
    return rows
